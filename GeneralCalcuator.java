package sampleJava2;

public class GeneralCalcuator {
	int a, b, result;
	double divResult;

	public int addition() {
		result = a + b;
		return result;
	}

	public int substract() {
		result = a - b;
		return result;
	}

	public int multiplication() {
		result = a * b;
		return result;
	}

	public double division() {
		divResult = a / b;
		return divResult;
	}
	
	public double remaainder() {
		divResult = a / b;
		return divResult;
	}
}
